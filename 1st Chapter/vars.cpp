#include <iostream>
using namespace std;

//A C++ Program to output variables.

int main()
{
	char letter;
	letter = 'A';

	int number;
	number = 100;

	float decimal = 7.5;
	double pi = 3.14159;
	bool isTrue = false;

	cout << "char letter: " << letter << endl;
	cout << "int number: " << number << endl;
	cout << "float decimal: " << decimal << endl;
	cout << "double pi: " << pi << endl;
	cout << "bool true: " << isTrue << endl;
	return 0;
}